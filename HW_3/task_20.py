import re
letters = {'[а, в, е, и, н, о, с, т, р]' : '1', '[д, к, л, м, п, у]' : '2', '[б, г, ё, ь, я]' : '3', '[й, ы]' : '4', '[ж, з, х, ч, ц]' : '5', '[ш, э, ю]' : '8', '[ф, щ, ъ]' : '10', '[a,e,i,o,u,l,n,s,t,r]' : '1', '[d,g]' : '2', '[b,c,m,p]' : '3', '[f,h,v,w,y]' : '4', '[k]' : '5', '[j,x]' : '8', '[q,z]' : '10'}
x = input('Введите одно любое слово: ')
for item in letters:
    x = re.sub(item, letters[item], x)
print(sum(map(int, x)))